// TestProviderCatalog_DownArrowAdvancesCursor verifies the row
// cursor walks forwards through the flattened catalog and that
// SelectedID actually changes.
func TestProviderCatalog_DownArrowAdvancesCursor(t *testing.T) {
	t.Parallel()

	m := New(MockOptions(""))
	mAny, _ := m.Update(tea.KeyMsg{Type: tea.KeyRunes, Runes: []rune("p")})
	m = mAny.(Model)

	first := catalogSnapshot(m).SelectedID
	if first == "" {
		t.Skip("registry empty; skipping cursor test")
	}

	mAny, _ = m.Update(tea.KeyMsg{Type: tea.KeyDown})
	m = mAny.(Model)
	second := catalogSnapshot(m).SelectedID
	if second == "" || second == first {
		t.Errorf("cursor did not advance: first=%q second=%q", first, second)
	}
}

// TestProviderCatalog_EnterTogglesDetail flips the deep-dive
// toggle. Two presses toggle on→off→on.
func TestProviderCatalog_EnterTogglesDetail(t *testing.T) {
	t.Parallel()

	m := New(MockOptions(""))
	mAny, _ := m.Update(tea.KeyMsg{Type: tea.KeyRunes, Runes: []rune("p")})
	m = mAny.(Model)

	if m.catalog.ShowDetail {
		t.Fatalf("setup: ShowDetail = %v, want false", m.catalog.ShowDetail)
	}
	mAny, _ = m.Update(tea.KeyMsg{Type: tea.KeyEnter})
	m = mAny.(Model)
	if !m.catalog.ShowDetail {
		t.Fatalf("after Enter: ShowDetail = false, want true")
	}
	mAny, _ = m.Update(tea.KeyMsg{Type: tea.KeyEnter})
	m = mAny.(Model)
	if m.catalog.ShowDetail {
		t.Fatalf("after second Enter: ShowDetail = true, want false")
	}
}

// TestProviderCatalog_CopyBriefingSuccessSetsHint stubs the
// clipboard runner with a no-op success and asserts the model
// reflects the green ack.
func TestProviderCatalog_CopyBriefingSuccessSetsHint(t *testing.T) {
	cleanup := setClipboardCommandRunner(func(_ string, _ ...string) clipboardCommand {
		return &fakeClipboardCommand{}
	})
	t.Cleanup(cleanup)

	m := New(MockOptions(""))
	mAny, _ := m.Update(tea.KeyMsg{Type: tea.KeyRunes, Runes: []rune("p")})
	m = mAny.(Model)

	mAny, _ = m.Update(tea.KeyMsg{Type: tea.KeyRunes, Runes: []rune("c")})
	m = mAny.(Model)
	if !strings.Contains(m.catalog.CopyHint, "copied") {
		t.Errorf("CopyHint after c = %q, want 'copied' substring", m.catalog.CopyHint)
	}
	if !m.catalog.ShowDetail {
		t.Errorf("ShowDetail after c = false, want true (briefing forces detail)")
	}
}

