// TestProviderCatalog_BriefingComposeContainsRequiredSections
// asserts the plain-text briefing carries every operator-visible
// field the View renders. Used as a regression guard so future
// schema bumps do not silently drop sections.
func TestProviderCatalog_BriefingComposeContainsRequiredSections(t *testing.T) {
	t.Parallel()

	m := New(MockOptions(""))
	mAny, _ := m.Update(tea.KeyMsg{Type: tea.KeyRunes, Runes: []rune("p")})
	m = mAny.(Model)
	mAny, _ = m.Update(tea.KeyMsg{Type: tea.KeyEnter})
	m = mAny.(Model)
	snap := catalogSnapshot(m)
	if snap.Detail.BriefingPlainText == "" {
		t.Skip("registry empty or no preset selected")
	}
	for _, needle := range []string{"Webox Provider Briefing", "id", "panel", "node runtime", "Generated by Webox Provider Catalog"} {
		if !strings.Contains(strings.ToLower(snap.Detail.BriefingPlainText), strings.ToLower(needle)) {
			t.Errorf("briefing missing %q\n--- briefing ---\n%s", needle, snap.Detail.BriefingPlainText)
		}
	}
}

