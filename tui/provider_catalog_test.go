// TestProviderCatalog_DashboardKeyOpensScreen confirms pressing
// `p` on the dashboard transitions to StateProviderCatalog.
func TestProviderCatalog_DashboardKeyOpensScreen(t *testing.T) {
	t.Parallel()

	m := New(MockOptions(""))
	updated, _ := m.Update(tea.KeyMsg{Type: tea.KeyRunes, Runes: []rune("p")})
	m = updated.(Model)
	if m.state != StateProviderCatalog {
		t.Fatalf("state after 'p' = %v, want StateProviderCatalog", m.state)
	}
}

